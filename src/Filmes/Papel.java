package Filmes;

public class Papel {
    private int nomePessoa;
    private String papel;

    public int getNomePessoa() {
        return nomePessoa;
    }

    public void setNomePessoa(int nomePessoa) {
        this.nomePessoa = nomePessoa;
    }

    public String getPapel() {
        return papel;
    }

    public void setPapel(String papel) {
        this.papel = papel;
    }
}
